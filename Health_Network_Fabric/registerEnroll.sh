#!/bin/bash

function createHospital() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/hospital.healthnetwork.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-hospital --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-hospital.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-hospital.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-hospital.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-hospital.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy hospital's CA cert to hospital's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/tlscacerts/ca.crt"

  # Copy hospital's CA cert to hospital's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/tlsca/tlsca.hospital.healthnetwork.com-cert.pem"

  # Copy hospital's CA cert to hospital's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/ca"
  cp "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/ca/ca.hospital.healthnetwork.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-hospital --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-hospital --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-hospital --id.name org1admin --id.secret org1adminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-hospital -M "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-hospital -M "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls" --enrollment.profile tls --csr.hosts peer0.hospital.healthnetwork.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/peers/peer0.hospital.healthnetwork.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-hospital -M "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/users/User1@hospital.healthnetwork.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/users/User1@hospital.healthnetwork.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://org1admin:org1adminpw@localhost:7054 --caname ca-hospital -M "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/users/Admin@hospital.healthnetwork.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/hospital/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/hospital.healthnetwork.com/users/Admin@hospital.healthnetwork.com/msp/config.yaml"
}

